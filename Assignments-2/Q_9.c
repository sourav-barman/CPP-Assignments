/*Program to print size of an int, a float, a char and a double type variable*/

#include <stdio.h>

int main() {
    printf("Size of an int type variable: %d\n", sizeof(int));
    printf("Size of a float type variable: %d\n", sizeof(float));
    printf("Size of a char type variable: %d\n", sizeof(char));
    printf("Size of a double type variable: %d\n", sizeof(double));

    return 0;
}